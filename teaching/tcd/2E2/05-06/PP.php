 <h2><b>Past papers</a></b></h2>

<p>All Maths department
papers since 1992 are online <a target=_parent
href="http://www.maths.tcd.ie/local/maths_exams/">here</a>. However,
you should note that the course was changed in 1999-2000.<p> There are
small changes to the course every year, the main change is in the amount of Z-transforms I cover, I used to do two questions worth, now I only do one, also, some years I did Green's theorem and not Gauss, whereas these days I do Gauss and not Green's. The heat equations was added when the amount of Z-transforms was reduced. This year I didn't cover Legendre polynomials or linearization with spirals.</p>

<p>I used to try and put up solutions for all questions, my policy now is to put up solutions to unusual questions or ones that I am asked about, so, please, if you are stuck on a question, email me and I will put up a solution as quickly as I can.</p>


