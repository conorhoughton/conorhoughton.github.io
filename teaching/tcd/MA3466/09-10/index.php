<p><b>Draft course description:</b> Information theory is the mathematical
study of the statistical structure of data, typical questions
addressed by information theory include "how much can English language
text be compressed" or "how similar are these two probability
distributions". As a discipline in mathematics it overlaps with
electronic engineering, computer science and neuroscience. This course
will provide an introduction to information theory based in the
standard text book Cover and Thomas, it will cover chapters 2, 3 and
4, along with material from chapters 5 and 6. Information theory uses
many ideas from statistics but no previous knowledge of statistics
will be assumed. </p>

