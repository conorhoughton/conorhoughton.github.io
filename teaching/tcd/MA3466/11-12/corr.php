<blockquote style="border: 0px solid #666; padding: 10px;
background-color: Lavender;"> <p> 21 April 2010<br> <b>typo</b> Hey, In your notes on Markov chains I think there's a typo in the definition. You say p(x|y,z)=p(x|y), instead of p(x|y)p(z|y). At least, that's what I hope it is... Is joint probability symmetric? It makes sense that it would be?<br>
<b>Reply</b> That isn't a typo: in a Markov chain X->Y->Z the probability distribution for X conditioned on Y and Z is the same as the probability distribution for X conditioned on Y on its own. This is the Markov property, X isn't independent of Z, but if you know Y knowing Z doesn't make any difference. You can think of a Markov chain as modelling an information flow, information flows from Z through Y to X so p(x|y,z)=p(x|y). Joint distrutions are symmetric p(x,y)=p(y,x) and, indeed, p(x|y,z)=p(x|z,y), that doesn't mean p(x|y) should be the same as p(x|z). Let me know if I haven't answered you question!
</p>
</blockquote>

