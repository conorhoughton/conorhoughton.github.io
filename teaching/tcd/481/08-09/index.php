<p><b>Draft course description:</b></p>

<p>This course will come in two version; 481 is a half course (5 ECTS)
with about 30 contact hours made up of lectures and tutorials. It will
cover more-or-less the same material that was covered last time 481
was taught, but this time there will be problem sheets and
tutorials. There will be a two hour end-of-year exam. 482 is a full
course (10 ECTS), it includes 481 along with <a href="http://www.maths.tcd.ie/~houghton/481++">481++</a> an introduction to c++ and a set of
three mandatory computer projects. These will be straight-forward
simulations related to neuroscience, somewhat in the spirit of the
computer demonstrations included in the course last time. Help will be
available in doing the project work, but passing 481++ will require
that all three projects are completed.</p>

<p>Mathematical neuroscience, as a whole, is divided into three main
areas, one of which relates to the dynamics of neurons themselves,
another to networks of neurons and a third to what people in the field
call neural encoding and decoding, the study of how stimulus and
response are related in neurons.</p>

<p>This course will consider each of areas in turn. It will begin with a
general introduction to neuroscience and no prior knowledge of
neurobiology will be assumed. Next, the voltage dynamics of neurons
will be considered with a description of the Hodkin-Huxley equation
and integrate-and-fire models. Spike train statistics will then be
considered, along with the neural code, correllation and reverse
correllation methods and stochastic properties. These ideas will be
illustrated with an introduction to the visual system. Finally,
neuronal plasticity and simple network models will be discussed.</p>

