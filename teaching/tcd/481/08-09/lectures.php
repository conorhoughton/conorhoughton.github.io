<p>
<b>Notes</b>
The course follows D&A in material but not in sequence.
<ul>
<li>After the introduction we do neuroelectrodynamics, chapter 5 of D&A, and the go back and cover the earlier material about coding and decoding.
<li>When studying neuroelectrodynamics, we skip the integrate and fire model and go straight to Hodgkin and Huxley and even its extensions (5.5, 5.6 and a bit of chapter 6) before going back to the integrate and fire (5.4).
<li>When doing coding and decoding we skip around a bit, leaving out Poisson Statistics, which are done later.
<li>After coding and decoding we skip forward to plasticity.
</ul>
